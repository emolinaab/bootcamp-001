class Calculator {
    addition(num1, num2) {
        return num1 + num2;
    }

    subtraction(num1, num2) {
        return num1 - num2;
    }

    divide(num1, num2) {
        return num1 / num2;
    }

    multiply(num1, num2) {
        return num1 * num2;
    }
    
    porcentage(num1, num2) {
        return num1 * num2/100;
    }
} 
