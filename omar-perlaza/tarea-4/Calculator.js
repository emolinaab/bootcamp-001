class Calculator {
  add(num1, num2) {
    let result = num1 + num2;
    return result;
  }

  subtract(num1, num2) {
    let result = num1 - num2;
    return result;
  }

  multiply(num1, num2) {
    let result = num1 * num2;
    return result;
  }

  divide(num1, num2) {
    let result = num1 / num2;
    return result;
  }
}
